// Smart matching logic to match student to local image file
export function getLocalImagePath(student) {
    if (!student) return '';
    
    // 1. Check explicit manual overrides first
    if (EXPLICIT_MAP[student.name]) {
        return `/img/${EXPLICIT_MAP[student.name]}`;
    }
    
    // 2. Standardize search targets
    const sName = student.name.toLowerCase().replace(/[^a-z0-9]/g, ' ').replace(/\s+/g, ' ').trim();
    const sFeatName = (student.featured_name || '').toLowerCase().replace(/[^a-z0-9]/g, ' ').replace(/\s+/g, ' ').trim();
    
    const nameWords = sName.split(' ').filter(w => w.length > 2);
    const featWords = sFeatName.split(' ').filter(w => w.length > 2);
    const allWords = [...new Set([...nameWords, ...featWords])];
    
    let bestMatch = null;
    let maxOverlap = 0;
    
    for (let i = 0; i < LOCAL_IMAGES.length; i++) {
        const file = LOCAL_IMAGES[i];
        const fNorm = file.substring(0, file.lastIndexOf('.')).toLowerCase().replace(/[^a-z0-9]/g, ' ').replace(/\s+/g, ' ').trim();
        
        let overlap = 0;
        allWords.forEach(word => {
            if (fNorm.includes(word)) {
                overlap++;
            }
        });
        
        if (sFeatName && fNorm.includes(sFeatName) && sFeatName.length > 3) {
            overlap += 3;
        }
        
        if (overlap > maxOverlap) {
            maxOverlap = overlap;
            bestMatch = file;
        }
    }
    
    // High-confidence threshold (at least 2 overlapping name components or featured name match)
    if (maxOverlap >= 2) {
        return `/img/${bestMatch}`;
    }
    
    return '';
}
